import 'package:flutter/material.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'package:google_sign_in/google_sign_in.dart';
// Aseg√∫rate de que los archivos 'register_screen.dart' y 'home_screen.dart' existan
import 'register_screen.dart';
import 'home_screen.dart';

class LoginScreen extends StatefulWidget {
  const LoginScreen({super.key});

  @override
  State<LoginScreen> createState() => _LoginScreenState();
}

class _LoginScreenState extends State<LoginScreen> {
  // ===================================
  // 1. CONTROLADORES Y ESTADO
  // ===================================
  final _emailController = TextEditingController();
  final _passwordController = TextEditingController();
  final FirebaseAuth _auth = FirebaseAuth.instance;
  final GoogleSignIn _googleSignIn = GoogleSignIn();
  bool _isLoading = false;

  @override
  void dispose() {
    _emailController.dispose();
    _passwordController.dispose();
    super.dispose();
  }

  // ===================================
  // 2. M√âTODOS DE UTILIDAD Y NAVEGACI√ìN
  // ===================================

  void _navigateToHome() {
    if (mounted) {
      Navigator.pushReplacement(
        context,
        MaterialPageRoute(builder: (context) => const HomeScreen()),
      );
    }
  }

  void _showSnackBar(String message) {
    if (mounted) {
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(SnackBar(content: Text(message)));
    }
  }

  // ===================================
  // 3. L√ìGICA DE LOGIN (CORREO/CONTRASE√ëA)
  // OBLIGANDO LA VERIFICACI√ìN DEL EMAIL
  // ===================================

  Future<void> _login() async {
    setState(() => _isLoading = true);

    try {
      // 3.1. Intentar iniciar sesi√≥n con correo y contrase√±a
      UserCredential userCredential = await _auth.signInWithEmailAndPassword(
        email: _emailController.text.trim(),
        password: _passwordController.text.trim(),
      );

      final User? user = userCredential.user;

      if (user != null) {
        // 3.2. OBLIGAR LA RECARGA DE DATOS: Necesario para obtener el estado actual de 'emailVerified'.
        await user.reload();

        // Obtener el estado actualizado del usuario despu√©s de la recarga
        final User? reloadedUser = _auth.currentUser;

        // 3.3. VERIFICACI√ìN DEL EMAIL
        if (reloadedUser != null && reloadedUser.emailVerified) {
          // Si est√° verificado, permite el acceso
          _showSnackBar('‚úÖ ¬°Inicio de sesi√≥n exitoso!');
          _navigateToHome();
        } else {
          // Si NO est√° verificado:

          // Cerrar sesi√≥n para que no quede logueado
          await _auth.signOut();

          // Mostrar mensaje y ofrecer reenv√≠o de enlace
          _showSnackBar(
            'üîí Cuenta no verificada. Revisa tu bandeja de entrada o spam para el enlace de verificaci√≥n.',
          );

          // Opcional: Reenviar correo
          if (user.email != null) {
            await user.sendEmailVerification();
          }
        }
      }
    } on FirebaseAuthException catch (e) {
      String message;
      // 3.4. Manejo de errores de autenticaci√≥n
      if (e.code == 'user-not-found' || e.code == 'wrong-password') {
        message = '‚ùå Error: Correo o contrase√±a incorrectos.';
      } else if (e.code == 'invalid-email') {
        message = '‚ùå Error: El formato del correo es inv√°lido.';
      } else {
        message = '‚ùå Error de inicio de sesi√≥n: ${e.message}';
      }
      _showSnackBar(message);
    } finally {
      if (mounted) {
        setState(() => _isLoading = false);
      }
    }
  }

  // -----------------------------------------------------------

  // ===================================
  // 4. L√ìGICA DE LOGIN (SOCIAL)
  // ===================================

  Future<void> _loginWithGoogle() async {
    setState(() => _isLoading = true);

    try {
      final GoogleSignInAccount? googleUser = await _googleSignIn.signIn();
      if (googleUser == null) {
        return; // Usuario cancel√≥
      }

      final GoogleSignInAuthentication googleAuth =
          await googleUser.authentication;
      final AuthCredential credential = GoogleAuthProvider.credential(
        accessToken: googleAuth.accessToken,
        idToken: googleAuth.idToken,
      );

      await _auth.signInWithCredential(credential);
      _navigateToHome();
    } on FirebaseAuthException catch (e) {
      _showSnackBar('Error con Google Sign-In: ${e.message}');
    } catch (e) {
      _showSnackBar('Ocurri√≥ un error inesperado al iniciar con Google.');
    } finally {
      if (mounted) {
        setState(() => _isLoading = false);
      }
    }
  }

  Future<void> _loginWithFacebook() async {
    _showSnackBar(
      'Facebook Login: Esta funcionalidad requiere configuraci√≥n adicional!',
    );
  }

  // -----------------------------------------------------------

  // =========================================================
  // 5. M√âTODOS PARA RECUPERACI√ìN DE CONTRASE√ëA
  // =========================================================

  Future<void> _sendPasswordResetEmail(String email) async {
    if (email.isEmpty) return;

    setState(() => _isLoading = true);

    try {
      await _auth.sendPasswordResetEmail(email: email);
      _showSnackBar(
        'Se ha enviado un correo de recuperaci√≥n a $email. Revisa tu bandeja de entrada.',
      );
    } on FirebaseAuthException catch (e) {
      String message;
      if (e.code == 'user-not-found') {
        message = 'No existe una cuenta con ese correo.';
      } else if (e.code == 'invalid-email') {
        message = 'El formato del correo es inv√°lido.';
      } else {
        message = 'Error al enviar el correo de recuperaci√≥n: ${e.message}';
      }
      _showSnackBar(message);
    } finally {
      if (mounted) {
        setState(() => _isLoading = false);
      }
    }
  }

  Future<void> _showResetPasswordDialog() async {
    final resetEmailController = TextEditingController();
    final formKey = GlobalKey<FormState>();

    return showDialog(
      context: context,
      builder: (BuildContext dialogContext) {
        return AlertDialog(
          title: const Text('Recuperar Contrase√±a'),
          content: Form(
            key: formKey,
            child: TextFormField(
              controller: resetEmailController,
              decoration: const InputDecoration(
                labelText: 'Ingresa tu correo electr√≥nico',
                hintText: 'ejemplo@correo.com',
              ),
              keyboardType: TextInputType.emailAddress,
              validator: (value) {
                if (value == null || value.isEmpty) {
                  return 'El correo es obligatorio.';
                }
                if (!value.contains('@') || !value.contains('.')) {
                  return 'Ingresa un correo v√°lido.';
                }
                return null;
              },
            ),
          ),
          actions: [
            TextButton(
              onPressed: () => Navigator.of(dialogContext).pop(),
              child: const Text('Cancelar'),
            ),
            ElevatedButton(
              onPressed: _isLoading
                  ? null
                  : () async {
                      if (formKey.currentState!.validate()) {
                        Navigator.of(dialogContext).pop();

                        await _sendPasswordResetEmail(
                          resetEmailController.text.trim(),
                        );
                      }
                    },
              child: _isLoading
                  ? const SizedBox(
                      width: 20,
                      height: 20,
                      child: CircularProgressIndicator(
                        strokeWidth: 2,
                        color: Colors.white,
                      ),
                    )
                  : const Text('Enviar Enlace'),
            ),
          ],
        );
      },
    );
  }

  // -----------------------------------------------------------

  // ===================================
  // 6. WIDGET BUILD (Interfaz de Usuario)
  // ===================================

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('Inicio de Sesi√≥n'), centerTitle: true),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(24.0),
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            // 1. Campos de Correo y Contrase√±a
            TextField(
              controller: _emailController,
              decoration: const InputDecoration(
                labelText: 'Correo Electr√≥nico',
                border: OutlineInputBorder(),
                prefixIcon: Icon(Icons.email),
              ),
              keyboardType: TextInputType.emailAddress,
            ),
            const SizedBox(height: 16),
            TextField(
              controller: _passwordController,
              decoration: const InputDecoration(
                labelText: 'Contrase√±a',
                border: OutlineInputBorder(),
                prefixIcon: Icon(Icons.lock),
              ),
              obscureText: true,
            ),

            const SizedBox(height: 12),

            // 2. Bot√≥n de Recuperar Contrase√±a
            Align(
              alignment: Alignment.centerRight,
              child: TextButton(
                onPressed: _isLoading ? null : _showResetPasswordDialog,
                child: const Text('Olvidaste tu contrase√±a?'),
              ),
            ),

            const SizedBox(height: 24),

            // 3. Bot√≥n de Iniciar Sesi√≥n
            SizedBox(
              width: double.infinity,
              child: _isLoading
                  ? const Center(child: CircularProgressIndicator())
                  : ElevatedButton(
                      onPressed: _login,
                      style: ElevatedButton.styleFrom(
                        padding: const EdgeInsets.symmetric(vertical: 15),
                        backgroundColor: Theme.of(context).primaryColor,
                        foregroundColor: Colors.white,
                      ),
                      child: const Text(
                        'Iniciar Sesi√≥n',
                        style: TextStyle(fontSize: 18),
                      ),
                    ),
            ),

            const SizedBox(height: 30),

            // 4. Separador "o"
            const Row(
              children: [
                Expanded(child: Divider()),
                Padding(
                  padding: EdgeInsets.symmetric(horizontal: 10),
                  child: Text(
                    'O inicia sesi√≥n con',
                    style: TextStyle(color: Colors.grey),
                  ),
                ),
                Expanded(child: Divider()),
              ],
            ),

            const SizedBox(height: 30),

            // 5. Botones de Redes Sociales
            Row(
              mainAxisAlignment: MainAxisAlignment.spaceEvenly,
              children: [
                // Bot√≥n de Google Sign-In
                SizedBox(
                  width: 150,
                  child: OutlinedButton.icon(
                    onPressed: _isLoading ? null : _loginWithGoogle,
                    icon: const Icon(Icons.g_mobiledata),
                    label: const Text('Google'),
                    style: OutlinedButton.styleFrom(
                      padding: const EdgeInsets.symmetric(vertical: 12),
                    ),
                  ),
                ),

                // Bot√≥n de Facebook Sign-In
                SizedBox(
                  width: 150,
                  child: OutlinedButton.icon(
                    onPressed: _isLoading ? null : _loginWithFacebook,
                    icon: const Icon(Icons.facebook, color: Colors.blue),
                    label: const Text('Facebook'),
                    style: OutlinedButton.styleFrom(
                      padding: const EdgeInsets.symmetric(vertical: 12),
                    ),
                  ),
                ),
              ],
            ),

            const SizedBox(height: 40),

            // 6. Enlace a la Pantalla de Registro
            TextButton(
              onPressed: () {
                Navigator.push(
                  context,
                  MaterialPageRoute(
                    builder: (context) => const RegisterScreen(),
                  ),
                );
              },
              child: const Text(
                'No tienes una cuenta? Reg√≠strate aqu√≠',
                style: TextStyle(decoration: TextDecoration.underline),
              ),
            ),
          ],
        ),
      ),
    );
  }
}
